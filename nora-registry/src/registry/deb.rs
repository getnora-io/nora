/// Dashboard index: group stored .deb files by repository name.
pub const INDEX_PATTERN: (&str, &str) = ("deb/", ".deb");

pub fn routes() -> Router<AppState> {
    Router::new()
        .route("/deb/{repo}/-/reindex", axum::routing::post(reindex))
        .route(
            "/deb/{repo}/{*path}",
            get(download)
                .head(check_exists)
                .put(upload)
                .delete(delete_package)
                .fallback(|| async { method_not_allowed("GET, HEAD, PUT, DELETE") }),
        )
}

/// Sidecar prefix inside a repo. Rejected as an upload path component, so it
/// can never collide with package files.
const META_DIR: &str = ".nora-meta";
/// Index files regenerated by the server at the repo root.
const RESERVED: &[&str] = &[
    "Packages",
    "Packages.gz",
    "Release",
    "InRelease",
    "Release.gpg",
];
/// Decompressed control.tar cap — control archives are a few KB; anything
/// larger is a decompression bomb, not a package (trust boundary).
const CONTROL_TAR_MAX: u64 = 8 * 1024 * 1024;

fn package_key(repo: &str, path: &str) -> String {
    format!("deb/{repo}/{path}")
}

fn sidecar_key(repo: &str, path: &str) -> String {
    format!("deb/{repo}/{META_DIR}/{path}.json")
}

fn release_key(repo: &str) -> String {
    format!("deb/{repo}/Release")
}

/// apt resolves a flat-repo sources line (`deb <url>/deb/{repo} ./`) to
/// literal `/deb/{repo}/./InRelease` etc. — strip that leading `./` on the
/// read paths. Only the prefix apt produces; traversal stays rejected.
fn strip_flat_prefix(path: &str) -> &str {
    path.strip_prefix("./").unwrap_or(path)
}

/// Validate the `{repo}` segment and the tail path of a package operation.
/// The storage-key wrapper re-validates centrally; this is edge defence with
/// format-specific rules (single-segment repo, `.deb` suffix, reserved names).
fn validate_package_path(repo: &str, path: &str) -> Result<(), &'static str> {
    if repo.is_empty() || !repo.is_ascii() || repo.contains('/') || repo.starts_with('.') {
        return Err("Invalid repository name");
    }
    if !path.is_ascii() || path.contains("..") || path.contains('\0') || path.starts_with('/') {
        return Err("Invalid path");
    }
    if !path.to_ascii_lowercase().ends_with(".deb") {
        return Err("Only .deb files can be published");
    }
    if path
        .split('/')
        .any(|seg| seg.is_empty() || seg.starts_with('.'))
    {
        return Err("Invalid path");
    }
    if RESERVED.contains(&path) || path.starts_with("dists/") {
        return Err("Reserved index path");
    }
    Ok(())
}

/// Structured-layout placement: the package is indexed under
/// `dists/{distribution}/{component}/binary-{arch}/`. Absent from a sidecar →
/// the package belongs to the flat root layout (pre-structured sidecars
/// deserialize as flat).
#[derive(Debug, Clone, Serialize, Deserialize)]
struct Placement {
    distribution: String,
    component: String,
}

#[derive(Debug, Default, Deserialize)]
struct PublishQuery {
    distribution: Option<String>,
    component: Option<String>,
}

/// A distribution/component/architecture name becomes one path segment under
/// `dists/` and one space-separated token in the Release `Components` /
/// `Architectures` lists — restrict to characters safe in both.
fn valid_release_token(s: &str) -> bool {
    !s.is_empty()
        && s.len() <= 64
        && !s.starts_with('.')
        && s.chars()
            .all(|c| c.is_ascii_alphanumeric() || matches!(c, '-' | '_' | '.' | '+'))
}

fn resolve_placement(q: PublishQuery) -> Result<Option<Placement>, &'static str> {
    let Some(distribution) = q.distribution else {
        return match q.component {
            None => Ok(None),
            Some(_) => Err("component requires distribution"),
        };
    };
    let component = q.component.unwrap_or_else(|| "main".to_string());
    if !valid_release_token(&distribution) || !valid_release_token(&component) {
        return Err("Invalid distribution/component name");
    }
    Ok(Some(Placement {
        distribution,
        component,
    }))
}

