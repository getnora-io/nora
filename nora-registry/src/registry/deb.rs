async fn upload(
    State(state): State<AppState>,
    Path((repo, path)): Path<(String, String)>,
    Query(query): Query<PublishQuery>,
    Extension(authority): Extension<NamespaceAuthority>,
    body: Bytes,
) -> Response {
    if !state.config.deb.enabled {
        return StatusCode::NOT_FOUND.into_response();
    }
    if state.config.deb.proxies.contains_key(&repo) {
        return proxied_repo_conflict();
    }
    if let Err(msg) = validate_package_path(&repo, &path) {
        return (StatusCode::BAD_REQUEST, msg).into_response();
    }
    let placement = match resolve_placement(query) {
        Ok(p) => p,
        Err(msg) => return (StatusCode::BAD_REQUEST, msg).into_response(),
    };
    // Enforce OIDC namespace_scope on the repository name (#583).
    if enforce_namespace_scope(&authority, &repo).is_err() {
        return StatusCode::FORBIDDEN.into_response();
    }
    if body.len() as u64 > state.config.deb.max_file_size {
        return (
            StatusCode::PAYLOAD_TOO_LARGE,
            format!(
                "File too large. Max size: {} bytes",
                state.config.deb.max_file_size
            ),
        )
            .into_response();
    }

    let record = match extract_record(&body, &path, placement) {
        Ok(r) => r,
        Err(e) => {
            return (StatusCode::BAD_REQUEST, format!("Not a valid deb: {e}")).into_response()
        }
    };
    let sidecar = match serde_json::to_vec(&record) {
        Ok(j) => j,
        Err(e) => {
            tracing::error!(error = %e, "deb: failed to serialize package record");
            return StatusCode::INTERNAL_SERVER_ERROR.into_response();
        }
    };

    let key = package_key(&repo, &path);
    if validate_storage_key(&key).is_err() {
        return StatusCode::BAD_REQUEST.into_response();
    }

    // Serialize publishes/deletes per repo: the index rebuild is a
    // list-read-generate-write cycle over the whole repo (same TOCTOU
    // rationale as the maven-metadata.xml lock).
    let lock = state.publish_lock(&release_key(&repo));
    let _guard = lock.lock().await;

    if let Err(e) = state.storage.put(&key, &body).await {
        tracing::error!(error = %e, key = %key, "deb: failed to store package");
        return StatusCode::INTERNAL_SERVER_ERROR.into_response();
    }
    if let Err(e) = state
        .storage
        .put(&sidecar_key(&repo, &path), &sidecar)
        .await
    {
        tracing::error!(error = %e, key = %key, "deb: failed to store metadata sidecar");
        return StatusCode::INTERNAL_SERVER_ERROR.into_response();
    }
    if let Err(e) = regenerate_indexes(&state.storage, state.signer.as_deref(), &repo).await {
        tracing::error!(repo = %repo, error = %e, "deb: index regeneration failed");
        return (
            StatusCode::INTERNAL_SERVER_ERROR,
            "Package stored but index regeneration failed",
        )
            .into_response();
    }

    let nva = format!("{}_{}_{}", record.package, record.version, record.arch);
    state.metrics.record_upload("deb");
    state
        .audit
        .log(AuditEntry::new("push", "api", &nva, "deb", ""));
    state.activity.push(ActivityEntry::new(
        ActionType::Push,
        format!("{repo}/{nva}"),
        crate::registry_type::RegistryType::Deb,
        "LOCAL",
    ));
    state.repo_index.invalidate("deb");

    StatusCode::CREATED.into_response()
}

async fn download(
    State(state): State<AppState>,
    Path((repo, path)): Path<(String, String)>,
) -> Response {
    if !state.config.deb.enabled {
        return StatusCode::NOT_FOUND.into_response();
    }
    let path = strip_flat_prefix(&path).to_string();
    // Pull-through repo: every path (including dists/ indexes and pubkey.gpg,
    // which are server-generated only for hosted repos) proxies to the
    // configured upstream. Packages are immutable; metadata is TTL-bounded.
    if let Some(entry) = state.config.deb.proxies.get(&repo) {
        let key = package_key(&repo, &path);
        if validate_storage_key(&key).is_err() || path.starts_with(META_DIR) {
            return StatusCode::BAD_REQUEST.into_response();
        }
        let lower = path.to_ascii_lowercase();
        let immutable = lower.ends_with(".deb") || lower.ends_with(".udeb");
        let url = format!("{}/{}", entry.url().trim_end_matches('/'), path);
        return crate::registry::repo_proxy_download(
            &state,
            "deb",
            crate::registry_type::RegistryType::Deb,
            format!("{repo}/{path}"),
            key,
            url,
            entry.auth(),
            state.config.deb.proxy_timeout,
            state.config.deb.metadata_ttl,
            immutable,
            content_type(&path),
        )
        .await;
    }
    if path == "pubkey.gpg" {
        return match &state.signer {
            Some(signer) => (
                StatusCode::OK,
                [(header::CONTENT_TYPE, "application/pgp-keys")],
                signer.public_key_armored().to_string(),
            )
                .into_response(),
            None => StatusCode::NOT_FOUND.into_response(),
        };
    }
    let key = package_key(&repo, &path);
    if validate_storage_key(&key).is_err() || path.starts_with(META_DIR) {
        return StatusCode::BAD_REQUEST.into_response();
    }

    match state.storage.get_verified(&key).await {
        Ok(outcome) => {
            state.metrics.record_download("deb");
            state.activity.push(ActivityEntry::new(
                ActionType::Pull,
                format!("{repo}/{path}"),
                crate::registry_type::RegistryType::Deb,
                "LOCAL",
            ));
            use nora_registry::verified::{verified_body, GateOutcome};
            let data = match outcome {
                GateOutcome::Verified(blob) => verified_body(blob),
                GateOutcome::Unpinned(blob) => blob.into_inner(),
            };
            let mut builder = axum::http::Response::builder()
                .status(StatusCode::OK)
                .header(header::CONTENT_TYPE, content_type(&path));
            // Index files are rewritten in place on every publish (fixed
            // names, no by-hash) — clients must revalidate.
            if RESERVED.contains(&path.as_str()) || path.starts_with("dists/") {
                builder = builder.header(header::CACHE_CONTROL, "no-cache");
            }
            builder
                .body(axum::body::Body::from(data))
                .expect("valid response")
                .into_response()
        }
        Err(crate::storage::StorageError::NotFound) => StatusCode::NOT_FOUND.into_response(),
        Err(e) => {
            tracing::error!(error = %e, key = %key, "deb: failed to read artifact");
            StatusCode::INTERNAL_SERVER_ERROR.into_response()
        }
    }
}

async fn check_exists(
    State(state): State<AppState>,
    Path((repo, path)): Path<(String, String)>,
) -> Response {
    if !state.config.deb.enabled {
        return StatusCode::NOT_FOUND.into_response();
    }
    let path = strip_flat_prefix(&path).to_string();
    let key = package_key(&repo, &path);
    if validate_storage_key(&key).is_err() || path.starts_with(META_DIR) {
        return StatusCode::BAD_REQUEST.into_response();
    }
    match state.storage.stat(&key).await {
        Some(meta) => (
            StatusCode::OK,
            [
                (header::CONTENT_LENGTH, meta.size.to_string()),
                (header::CONTENT_TYPE, content_type(&path).to_string()),
            ],
        )
            .into_response(),
        None => StatusCode::NOT_FOUND.into_response(),
    }
}

async fn delete_package(
    State(state): State<AppState>,
    Path((repo, path)): Path<(String, String)>,
    Extension(authority): Extension<NamespaceAuthority>,
) -> Response {
    if !state.config.deb.enabled {
        return StatusCode::NOT_FOUND.into_response();
    }
    if state.config.deb.proxies.contains_key(&repo) {
        return proxied_repo_conflict();
    }
    if let Err(msg) = validate_package_path(&repo, &path) {
        return (StatusCode::BAD_REQUEST, msg).into_response();
    }
    if enforce_namespace_scope(&authority, &repo).is_err() {
        return StatusCode::FORBIDDEN.into_response();
    }
    let key = package_key(&repo, &path);
    if validate_storage_key(&key).is_err() {
        return StatusCode::BAD_REQUEST.into_response();
    }

    let lock = state.publish_lock(&release_key(&repo));
    let _guard = lock.lock().await;

    match state.storage.delete(&key).await {
        Ok(()) => {}
        Err(crate::storage::StorageError::NotFound) => {
            return StatusCode::NOT_FOUND.into_response()
        }
        Err(e) => {
            tracing::error!(error = %e, key = %key, "deb: failed to delete package");
            return StatusCode::INTERNAL_SERVER_ERROR.into_response();
        }
    }
    if let Err(e) = state.storage.delete(&sidecar_key(&repo, &path)).await {
        tracing::warn!(error = %e, key = %key, "deb: failed to delete metadata sidecar");
    }
    if let Err(e) = regenerate_indexes(&state.storage, state.signer.as_deref(), &repo).await {
        tracing::error!(repo = %repo, error = %e, "deb: index regeneration failed");
        return (
            StatusCode::INTERNAL_SERVER_ERROR,
            "Package deleted but index regeneration failed",
        )
            .into_response();
    }

    state
        .audit
        .log(AuditEntry::new("delete", "api", &path, "deb", ""));
    state.repo_index.invalidate("deb");
    StatusCode::NO_CONTENT.into_response()
}

/// Reconcile a repository with what is actually in storage, then rebuild
/// (and re-sign) its indexes — the deb counterpart of the rpm reindex; see
/// that handler for the contract. Runs under the repo publish lock.
///
/// Packages found without a sidecar are adopted into the *flat* layout: a
/// structured placement exists only as upload-time intent, so it cannot be
/// reconstructed from storage. Re-upload with `?distribution=` to place an
/// adopted package under dists/.
async fn reindex(
    State(state): State<AppState>,
    Path(repo): Path<String>,
    Extension(authority): Extension<NamespaceAuthority>,
) -> Response {
    if !state.config.deb.enabled {
        return StatusCode::NOT_FOUND.into_response();
    }
    if repo.is_empty() || !repo.is_ascii() || repo.contains('/') || repo.starts_with('.') {
        return (StatusCode::BAD_REQUEST, "Invalid repository name").into_response();
    }
    if state.config.deb.proxies.contains_key(&repo) {
        return proxied_repo_conflict();
    }
    if enforce_namespace_scope(&authority, &repo).is_err() {
        return StatusCode::FORBIDDEN.into_response();
    }

    let lock = state.publish_lock(&release_key(&repo));
    let _guard = lock.lock().await;

    let prefix = format!("deb/{repo}/");
    let keys = match state.storage.list(&prefix).await {
        Ok(k) => k,
        Err(e) => {
            tracing::error!(error = %e, repo = %repo, "deb reindex: list failed");
            return StatusCode::INTERNAL_SERVER_ERROR.into_response();
        }
    };
    if keys.is_empty() {
        return (StatusCode::NOT_FOUND, "No such repository").into_response();
    }

    let meta_prefix = format!("deb/{repo}/{META_DIR}/");
    let mut packages: std::collections::BTreeSet<String> = std::collections::BTreeSet::new();
    let mut sidecars: std::collections::BTreeSet<String> = std::collections::BTreeSet::new();
    for key in &keys {
        if let Some(rest) = key.strip_prefix(&meta_prefix) {
            if let Some(pkg) = rest.strip_suffix(".json") {
                sidecars.insert(pkg.to_string());
            }
        } else if let Some(rest) = key.strip_prefix(&prefix) {
            if rest.to_ascii_lowercase().ends_with(".deb") {
                packages.insert(rest.to_string());
            }
        }
    }

    let mut orphans_removed = 0usize;
    for stale in sidecars.difference(&packages) {
        match state.storage.delete(&sidecar_key(&repo, stale)).await {
            Ok(()) => orphans_removed += 1,
            Err(e) => {
                tracing::error!(error = %e, repo = %repo, pkg = %stale, "deb reindex: orphan sidecar delete failed");
                return StatusCode::INTERNAL_SERVER_ERROR.into_response();
            }
        }
    }

    let mut sidecars_created = 0usize;
    for missing in packages.difference(&sidecars) {
        let body = match state.storage.get(&package_key(&repo, missing)).await {
            Ok(b) => b,
            Err(e) => {
                tracing::error!(error = %e, repo = %repo, pkg = %missing, "deb reindex: package read failed");
                return StatusCode::INTERNAL_SERVER_ERROR.into_response();
            }
        };
        let record = match extract_record(&body, missing, None) {
            Ok(r) => r,
            Err(e) => {
                tracing::error!(error = %e, repo = %repo, pkg = %missing, "deb reindex: not a valid deb — refusing to index");
                return (
                    StatusCode::UNPROCESSABLE_ENTITY,
                    format!("{missing} is not a valid deb: {e}"),
                )
                    .into_response();
            }
        };
        let json = match serde_json::to_vec(&record) {
            Ok(j) => j,
            Err(e) => {
                tracing::error!(error = %e, "deb reindex: sidecar serialize failed");
                return StatusCode::INTERNAL_SERVER_ERROR.into_response();
            }
        };
        if let Err(e) = state.storage.put(&sidecar_key(&repo, missing), &json).await {
            tracing::error!(error = %e, repo = %repo, pkg = %missing, "deb reindex: sidecar write failed");
            return StatusCode::INTERNAL_SERVER_ERROR.into_response();
        }
        sidecars_created += 1;
    }

    if let Err(e) = regenerate_indexes(&state.storage, state.signer.as_deref(), &repo).await {
        tracing::error!(repo = %repo, error = %e, "deb reindex: index regeneration failed");
        return StatusCode::INTERNAL_SERVER_ERROR.into_response();
    }

    state
        .audit
        .log(AuditEntry::new("reindex", "api", &repo, "deb", ""));
    state.repo_index.invalidate("deb");

    (
        StatusCode::OK,
        axum::Json(serde_json::json!({
            "packages": packages.len(),
            "sidecars_created": sidecars_created,
            "orphans_removed": orphans_removed,
            "signed": state.signer.is_some(),
        })),
    )
        .into_response()
}

fn content_type(path: &str) -> &'static str {
    if path.ends_with(".deb") {
        "application/vnd.debian.binary-package"
    } else if path.ends_with(".gz") {
        "application/gzip"
    } else {
        "text/plain; charset=utf-8"
    }
}

