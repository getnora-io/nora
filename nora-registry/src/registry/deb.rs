        let resp = send(&ctx.app, Method::GET, "/deb/myrepo/Release", "").await;
        assert_eq!(resp.status(), StatusCode::OK);
        let release = String::from_utf8(body_bytes(resp).await.to_vec()).unwrap();
        let pkg_sha = hex::encode(sha2::Sha256::digest(packages.as_bytes()));
        let gz_sha = hex::encode(sha2::Sha256::digest(&gz));
