/// Everything the Packages index needs for one package: the verbatim control
/// paragraph plus the fields computed from the uploaded bytes.
#[derive(Debug, Clone, Serialize, Deserialize)]
struct PkgRecord {
    package: String,
    version: String,
    arch: String,
    /// Verbatim control paragraph, trailing whitespace trimmed.
    control: String,
    /// Location relative to the repo root (= the upload path).
    filename: String,
    size: u64,
    md5: String,
    sha1: String,
    sha256: String,
}

impl PkgRecord {
    /// Render the Packages-file paragraph: control fields verbatim, then the
    /// repository-computed fields (dpkg ignores field order).
    fn paragraph(&self) -> String {
        format!(
            "{}\nFilename: {}\nSize: {}\nMD5sum: {}\nSHA1: {}\nSHA256: {}\n",
            self.control.trim_end(),
            self.filename,
            self.size,
            self.md5,
            self.sha1,
            self.sha256
        )
    }
}

/// Fields the repository computes itself and appends to each paragraph — an
/// uploaded control carrying its own copy would produce duplicate (and
/// contradictory) fields in the Packages index.
const COMPUTED_FIELDS: &[&str] = &["filename", "size", "md5sum", "sha1", "sha256"];

/// Validate an uploaded control paragraph before it is embedded verbatim in
/// the shared Packages index. dpkg-deb can only build well-formed controls,
/// but this endpoint accepts anything ar/tar-shaped, and one crafted control
/// corrupts the whole repo's index: an embedded blank line splits the
/// paragraph (forging a second stanza), a duplicated Filename/SHA256
/// contradicts the computed one, and C0 bytes trip apt's parser.
fn validate_control(control: &str) -> Result<(), String> {
    for (i, line) in control.trim_end().lines().enumerate() {
        if line.trim().is_empty() {
            return Err("control contains a blank line (paragraph injection)".into());
        }
        if line.chars().any(|c| c.is_ascii_control() && c != '\t') {
            return Err("control contains a control character".into());
        }
        if line.starts_with(' ') || line.starts_with('\t') {
            if i == 0 {
                return Err("control starts with a continuation line".into());
            }
            continue;
        }
        let Some((field, _)) = line.split_once(':') else {
            return Err(format!(
                "control line {} is not a field or continuation",
                i + 1
            ));
        };
        if field.is_empty() || !field.chars().all(|c| c.is_ascii_graphic() && c != ':') {
            return Err(format!("control line {} has an invalid field name", i + 1));
        }
        if COMPUTED_FIELDS.contains(&field.to_ascii_lowercase().as_str()) {
            return Err(format!(
                "control must not carry repository-computed field {field}"
            ));
        }
    }
    Ok(())
}

fn extract_record(body: &[u8], path: &str) -> Result<PkgRecord, String> {
    let control = extract_control(body)?;
    validate_control(&control)?;
    let package = control_field(&control, "Package")
        .filter(|v| !v.is_empty())
        .ok_or("control missing Package field")?;
    let version = control_field(&control, "Version")
        .filter(|v| !v.is_empty())
        .ok_or("control missing Version field")?;
    let arch = control_field(&control, "Architecture")
        .filter(|v| !v.is_empty())
        .ok_or("control missing Architecture field")?;
    Ok(PkgRecord {
        package: package.to_string(),
        version: version.to_string(),
        arch: arch.to_string(),
        control: control.trim_end().to_string(),
        filename: path.to_string(),
        size: body.len() as u64,
        md5: hex::encode(md5::Md5::digest(body)),
        sha1: hex::encode(sha1::Sha1::digest(body)),
        sha256: hex::encode(sha2::Sha256::digest(body)),
    })
}

/// RFC 1123 date as apt expects in Release (`Thu, 10 Jul 2026 12:00:00 UTC`).
fn release_date() -> String {
    chrono::Utc::now()
        .format("%a, %d %b %Y %H:%M:%S UTC")
        .to_string()
}

fn generate_release(packages: &[u8], packages_gz: &[u8], repo: &str) -> String {
    let entry = |data: &[u8], name: &str, hasher: fn(&[u8]) -> String| {
        format!(" {} {} {}\n", hasher(data), data.len(), name)
    };
    let md5 = |d: &[u8]| hex::encode(md5::Md5::digest(d));
    let sha256 = |d: &[u8]| hex::encode(sha2::Sha256::digest(d));
    format!(
        "Origin: NORA\nLabel: {repo}\nDate: {}\nMD5Sum:\n{}{}SHA256:\n{}{}",
        release_date(),
        entry(packages, "Packages", md5),
        entry(packages_gz, "Packages.gz", md5),
        entry(packages, "Packages", sha256),
        entry(packages_gz, "Packages.gz", sha256),
    )
}

/// Rebuild Packages/Packages.gz/Release for `repo` from control sidecars.
/// Caller must hold the repo's publish lock. Fail-closed: any error aborts the
/// rebuild (and the surrounding publish) rather than writing a truncated
/// index — same contract as the cargo index and the rpm handler.
///
/// Indexes have fixed names (flat apt repos have no by-hash), so a client that
/// fetched Release mid-regeneration can see a hash mismatch on Packages; apt
/// reports it and succeeds on retry — same window as reprepro/aptly.
async fn regenerate_indexes(state: &AppState, repo: &str) -> Result<(), String> {
    let meta_prefix = format!("deb/{repo}/{META_DIR}/");
    let keys = state
        .storage
        .list(&meta_prefix)
        .await
        .map_err(|e| format!("list sidecars: {e}"))?;

    let mut pkgs = Vec::with_capacity(keys.len());
    for key in &keys {
        let data = state
            .storage
            .get(key)
            .await
            .map_err(|e| format!("read sidecar {key}: {e}"))?;
        let rec: PkgRecord =
            serde_json::from_slice(&data).map_err(|e| format!("parse sidecar {key}: {e}"))?;
        pkgs.push(rec);
    }
    // Deterministic output: same package set → byte-identical indexes
    // (modulo the Release Date field).
    pkgs.sort_by(|a, b| {
        (&a.package, &a.version, &a.filename).cmp(&(&b.package, &b.version, &b.filename))
    });

    let packages: Vec<u8> = pkgs
        .iter()
        .map(|p| p.paragraph())
        .collect::<Vec<_>>()
        .join("\n")
        .into_bytes();

    let mut enc = GzEncoder::new(Vec::new(), flate2::Compression::default());
    let packages_gz = enc
        .write_all(&packages)
        .and_then(|_| enc.finish())
        .map_err(|e| format!("gzip Packages: {e}"))?;
    let release = generate_release(&packages, &packages_gz, repo);

    // Packages before Release — Release carries their hashes, so a reader
    // never sees a Release referencing index bytes that are not there yet.
    // Signatures last, for the same reason. Fail-closed like the rest of
    // the rebuild: a repo that claims to be signed must never publish an
    // unsigned or stale-signed index (#128).
    let mut files: Vec<(&str, Vec<u8>)> = vec![
        ("Packages", packages),
        ("Packages.gz", packages_gz),
        ("Release", release.clone().into_bytes()),
    ];
    match &state.signer {
        Some(signer) => {
            files.push(("InRelease", signer.clearsign(&release)?.into_bytes()));
            files.push((
                "Release.gpg",
                signer.sign_detached(release.as_bytes())?.into_bytes(),
            ));
        }
        None => {
            // Signing turned off after having been on: stale signatures that
            // no longer match Release would hard-fail apt's verification.
            for name in ["InRelease", "Release.gpg"] {
                let key = format!("deb/{repo}/{name}");
                if state.storage.stat(&key).await.is_some() {
                    state
                        .storage
                        .delete(&key)
                        .await
                        .map_err(|e| format!("delete stale {name}: {e}"))?;
                }
            }
        }
    }
    for (name, data) in files {
        state
            .storage
            .put(&format!("deb/{repo}/{name}"), &data)
            .await
            .map_err(|e| format!("write {name}: {e}"))?;
    }
    Ok(())
}

